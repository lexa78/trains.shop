<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;4.2. В случае невыполнения Исполнителем сроков оказания услуг, указанных в Протоколе согласования договорной цены (Приложениях № 5 к настоящему Договору), Заказчик имеет право потребовать, а Исполнитель обязан оплатить Заказчику пени в размере 0,05% (Пять сотых процента) от стоимости не оказанных вовремя услуг за каждый день просрочки.</p>
