<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;5.2. При наступлении обстоятельств непреодолимой силы Сторона, подвергшаяся влиянию таких обстоятельств, должна в течение 10 (Десяти) дней известить о них в письменном виде другую Сторону. В извещении должны быть сообщены данные о характере обстоятельств и их продолжительности, оценка их влияния на возможность исполнения обязательств по Договору. По прекращении указанных обстоятельств, Сторона должна без промедления известить об этом другую Сторону в письменном виде. В извещении должен быть указан срок, в котором предполагается исполнить обязательства по настоящему Договору. Если Сторона не направит или несвоевременно направит извещение, то она не вправе ссылаться на наличие обстоятельств непреодолимой силы как на основание освобождения от ответственности за неисполнение своих обязательств по настоящему Договору. К извещению должно быть приложено свидетельство/иной документ компетентного государственного органа, свидетельствующего наличие форс-мажорных обстоятельств.</p>
