<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;3.4. Не позднее 5 (Пяти) рабочих дней от даты получения комплекта документов согласно п. 2.1.5., Заказчик подписывает Акт сдачи-приемки оказанных услуг, составленный на последний день отчетного месяца (Приложение №4 к настоящему Договору). Счета-фактуры выставляются Исполнителем не позднее 5 (Пятого) числа месяца, следующего за месяцем оказания услуги, при этом дата счета-фактуры будет соответствовать дате Акта сдачи-приемки оказанных услуг.</p>
<p>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;3.5. Окончательный расчет за оказанные услуги производится в течение 5 календарных дней с момента подписания Сторонами Акта сдачи-приемки оказанных услуг. Переплата Заказчику не возвращается и засчитывается в счет оплаты следующих оказываемых услуг по вагонам Заказчика.</p>
